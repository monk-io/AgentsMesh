# 初始化 Gitea Git 服务器
init_gitea() {
    local gitea_container="${COMPOSE_PROJECT_NAME}-gitea-1"
    source "$ENV_FILE"
    local gitea_port="${GITEA_HTTP_PORT:-3001}"

    # Wait for Gitea healthcheck
    info "等待 Gitea 就绪..."
    local max_retries=30
    for ((i=1; i<=max_retries; i++)); do
        if curl -s "http://localhost:${gitea_port}/api/v1/version" &>/dev/null; then
            break
        fi
        if [[ $i -eq $max_retries ]]; then
            warn "Gitea 启动超时，跳过初始化"
            return 0
        fi
        sleep 2
    done
    success "Gitea 已就绪"

    # Run init script
    "$SCRIPT_DIR/gitea/init-gitea.sh" "$gitea_container" "$gitea_port"
}

# Generate the runner SSH keypair used to access Gitea repos.
# The private key is never committed; if it's missing (first clone, new machine),
# generate a fresh pair. init_gitea will register the new public key with Gitea.
#
# Also validates existing key pairs: if id_ed25519 and id_ed25519.pub are
# mismatched (e.g., one was regenerated without the other), SSH will refuse to
# authenticate with "identity_sign: private key contents do not match public".
# We detect this and regenerate both files to fix it automatically.
generate_runner_ssh_key() {
    local ssh_dir="$SCRIPT_DIR/runner-ssh"
    local private_key="$ssh_dir/id_ed25519"
    local public_key="$ssh_dir/id_ed25519.pub"

    if [[ -f "$private_key" ]]; then
        # Validate that the key pair is consistent.
        # OpenSSH will reject the key with a cryptic "contents do not match
        # public" error if the .pub file is stale or missing.
        local needs_regen=false

        if [[ ! -f "$public_key" ]]; then
            warn "Public key missing, will regenerate SSH key pair"
            needs_regen=true
        else
            # Derive the public key from the private key and compare with .pub file.
            # Only compare key type + key data (fields 1-2), ignoring the comment.
            local derived_key stored_key
            derived_key=$(ssh-keygen -y -f "$private_key" 2>/dev/null | awk '{print $1, $2}')
            stored_key=$(awk '{print $1, $2}' "$public_key" 2>/dev/null)

            if [[ -z "$derived_key" || "$derived_key" != "$stored_key" ]]; then
                warn "SSH key pair mismatch detected, will regenerate"
                needs_regen=true
            fi
        fi

        if [[ "$needs_regen" == true ]]; then
            rm -f "$private_key" "$public_key"
        else
            chmod 600 "$private_key"
            info "Runner SSH key already exists and is valid"
            return 0
        fi
    fi

    info "Generating runner SSH key (private key not committed)..."
    ssh-keygen -t ed25519 -C "agentsmesh-dev-runner@local" -f "$private_key" -N "" > /dev/null
    chmod 600 "$private_key"
    success "Runner SSH key generated: $private_key"
}

# Configure ~/.ssh/config so that local runners (running on the host, outside Docker)
# can clone git@gitea:org/repo.git URLs. Inside Docker, "gitea" resolves via Docker DNS.
# On the host, SSH would fail with "nodename not known" — we fix this by adding a managed
# Host block that maps "gitea" → 127.0.0.1 on the worktree-specific SSH port.
#
# Safe to re-run: removes the old managed block and rewrites it with the current port.
setup_gitea_ssh_config() {
    source "$ENV_FILE"
    local ssh_dir="$HOME/.ssh"
    local ssh_config="$ssh_dir/config"
    local gitea_ssh_port="${GITEA_SSH_PORT:-2222}"
    local identity_file="$SCRIPT_DIR/runner-ssh/id_ed25519"
    local marker_start="# BEGIN AgentsMesh dev gitea"
    local marker_end="# END AgentsMesh dev gitea"

    mkdir -p "$ssh_dir"
    chmod 700 "$ssh_dir"
    [[ -f "$ssh_config" ]] || touch "$ssh_config"
    chmod 600 "$ssh_config"

    # Remove any existing managed block so we can re-append with the current port.
    local tmp
    tmp=$(mktemp)
    awk "/^${marker_start}$/,/^${marker_end}$/{next} {print}" "$ssh_config" > "$tmp"
    cat "$tmp" > "$ssh_config"
    rm -f "$tmp"

    cat >> "$ssh_config" << EOF

${marker_start}
Host gitea
    HostName 127.0.0.1
    Port ${gitea_ssh_port}
    IdentityFile ${identity_file}
    IdentitiesOnly yes
    StrictHostKeyChecking no
    UserKnownHostsFile /dev/null
${marker_end}
EOF

    success "SSH config: git@gitea:... → 127.0.0.1:${gitea_ssh_port} (key: runner-ssh/id_ed25519)"
}

# Kill all stale runner-related processes and restart Docker runner/relay containers.
# Use this when runners are in a bad state and you want a clean slate.
reset_runners() {
    if [[ -f "$ENV_FILE" ]]; then
        source "$ENV_FILE"
    fi

    echo ""
    echo "=========================================="
    echo "  Reset Runner (rebuild bazel binary + restart container)"
    echo "=========================================="
    echo ""

    # 1. Optional: kill any externally-installed runner CLI process.
    if pgrep -f "agentsmesh-runner" &>/dev/null; then
        info "停止本地 agentsmesh-runner 进程..."
        pkill -f "agentsmesh-runner" 2>/dev/null || true
        sleep 1
        pkill -9 -f "agentsmesh-runner" 2>/dev/null || true
    fi

    # 2. Rebuild runner binary via Bazel (linux/amd64 cross-compile).
    build_runner_binary || return 1

    # 3. Rebuild + restart the runner container so the new binary is
    #    picked up. `--build` forces a fresh COPY of bazel-bin/.../runner.
    cd "$SCRIPT_DIR"
    info "重建并重启 runner 容器..."
    docker compose up -d --build runner 2>&1 | grep -v "^#" | grep -v warning || true
    success "Runner 容器已重启 (binary 来自 bazel build)"

    echo ""
}

# 清理环境
clean() {
    # 读取端口配置
    if [[ -f "$ENV_FILE" ]]; then
        source "$ENV_FILE"
    fi
    local web_port="${WEB_PORT:-3000}"

    local web_admin_port="${WEB_ADMIN_PORT:-3001}"

    # 停止 host-side ibazel services (backend / runner / relay)
    info "停止 host-side ibazel 服务..."
    stop_host_services
    success "host-side 服务已停止"

    # 停止前端进程
    if lsof -i :"$web_port" &>/dev/null; then
        info "停止前端服务 (端口: $web_port)..."
        lsof -ti :"$web_port" | xargs kill -9 2>/dev/null || true
        success "前端服务已停止"
    fi

    # 停止 Admin Console 进程
    if lsof -i :"$web_admin_port" &>/dev/null; then
        info "停止 Admin Console (端口: $web_admin_port)..."
        lsof -ti :"$web_admin_port" | xargs kill -9 2>/dev/null || true
        success "Admin Console 已停止"
    fi

    # 清理前端日志
    rm -f "$SCRIPT_DIR/web.log"
    rm -f "$SCRIPT_DIR/web-admin.log"
    # 清理 host-side 运行时状态 (pid/log/cert/runner workspace + isolated HOME)
    rm -rf "$(_runtime_dir)"

    if [[ -f "$ENV_FILE" ]]; then
        info "清理 Docker 环境: ${COMPOSE_PROJECT_NAME:-agentsmesh}..."
        cd "$SCRIPT_DIR"
        docker compose down -v --remove-orphans 2>/dev/null || true
        rm -f "$ENV_FILE"
        success "清理完成"
    else
        warn "Docker 环境未初始化"
    fi
}

