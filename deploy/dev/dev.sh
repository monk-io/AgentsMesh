# 主流程
main() {
    cd "$SCRIPT_DIR"

    # 处理 --clean 参数
    if [[ "${1:-}" == "--clean" || "${1:-}" == "-c" ]]; then
        clean
        exit 0
    fi

    # 处理 --reset-runners 参数
    if [[ "${1:-}" == "--reset-runners" || "${1:-}" == "--kill-runners" ]]; then
        reset_runners
        exit 0
    fi

    # 显示帮助
    if [[ "${1:-}" == "--help" || "${1:-}" == "-h" ]]; then
        echo "用法: $0 [选项]"
        echo ""
        echo "选项:"
        echo "  无参数           一键启动完整开发环境（Docker 后端 + 本地前端）"
        echo "  --clean          停止并清理所有服务"
        echo "  --reset-runners  终止所有 runner 进程并重启 Docker runner/relay 容器"
        echo ""
        echo "前端日志: tail -f deploy/dev/web.log"
        exit 0
    fi

    echo ""
    echo "=========================================="
    echo "  AgentsMesh 开发环境初始化"
    echo "=========================================="
    echo ""

    # Step 1: 生成 SSL 证书
    generate_ssl_certs

    # Step 2: 生成 AI CLI 配置
    generate_ai_cli_configs

    # Step 3: 生成配置
    generate_env
    source "$ENV_FILE"

    # Step 4: 生成 Traefik 配置（动态网络名，解决多 worktree 路由冲突）
    generate_traefik_config

    # Step 5: 生成 web/.env.local（支持本地前端开发）
    generate_web_env

    # Step 5.5: 生成 web-admin/.env.local（支持本地 Admin Console 开发）
    generate_web_admin_env

    # Step 5.8: Generate runner SSH key pair (must exist before Docker starts,
    # since the runner container bind-mounts runner-ssh/ as ~/.ssh)
    generate_runner_ssh_key

    # Step 6: 启动 Docker 后端服务
    info "启动 Docker 服务 (首次可能需要几分钟)..."
    docker compose up -d --build --quiet-pull 2>&1 | grep -v "^#" | grep -v "^\[" | grep -v "^$" || true
    success "Docker 服务已启动"

    # Step 7: 等待 PostgreSQL
    local pg_container="${COMPOSE_PROJECT_NAME}-postgres-1"
    info "等待 PostgreSQL 就绪..."
    if ! wait_for_service "$pg_container" "pg_isready -U agentsmesh"; then
        error "PostgreSQL 启动超时"
        exit 1
    fi
    success "PostgreSQL 已就绪"

    # Step 8: 执行迁移（使用 backend 容器中的 migrate 工具）
    run_migrations

    # Step 9: 初始化 seed
    init_seed "$pg_container"

    # Step 9.5: 初始化 Gitea Git 服务器 (SSH key already generated at Step 5.8)
    init_gitea

    # Step 9.6: Configure local SSH so git@gitea:... works outside Docker
    setup_gitea_ssh_config

    # Step 10: 修复 workspace 权限 (runner 容器)
    local runner_container="${COMPOSE_PROJECT_NAME}-runner-1"
    docker exec -u root "$runner_container" chown -R runner:runner /workspace 2>/dev/null || true

    # Step 11: 启动本地前端
    start_frontend

    # Step 11.5: 启动本地 Admin Console
    start_admin_frontend

    # 显示结果
    show_result
}

main "$@"
