# 启动本地前端
start_frontend() {
    source "$ENV_FILE"
    local web_dir="$SCRIPT_DIR/../../clients/web"
    local web_port="${WEB_PORT:-3000}"
    local lock_file="$web_dir/.next/dev/lock"
    local stale_lock=false

    # Kill any stale Next.js process holding the lock, then clear it
    if [[ -f "$lock_file" ]]; then
        warn "检测到残留的 Next.js 锁文件，清理中..."
        pkill -f "next dev" 2>/dev/null || true
        # Also kill anything still holding the port (port may outlive the process briefly)
        lsof -ti :"$web_port" 2>/dev/null | xargs kill -9 2>/dev/null || true
        sleep 1
        rm -f "$lock_file"
        rm -rf "$web_dir/.next/cache"
        success "锁文件和缓存已清理"
        stale_lock=true
    fi

    # 检查是否已有前端进程在运行（端口占用）
    # Skip this check if we just cleaned a stale lock — always restart in that case
    if [[ "$stale_lock" == false ]] && lsof -i :"$web_port" &>/dev/null; then
        warn "端口 $web_port 已被占用，跳过前端启动"
        return 0
    fi

    # 检查 bazel + pnpm（Bazel-driven dev workflow，但仍需 pnpm 解析外部依赖）
    if ! command -v bazel &>/dev/null; then
        error "未找到 bazel"
        return 1
    fi
    if ! command -v pnpm &>/dev/null; then
        error "未找到 pnpm，请先安装: npm install -g pnpm"
        return 1
    fi

    # 检查依赖（根目录 lockfile 变化时重新安装）
    local root_dir="$SCRIPT_DIR/../.."
    local lockfile="$root_dir/pnpm-lock.yaml"
    local lockfile_hash_file="$root_dir/node_modules/.pnpm-lock-hash"
    local current_hash=""
    local cached_hash=""
    [[ -f "$lockfile" ]] && current_hash=$(md5 -q "$lockfile" 2>/dev/null || md5sum "$lockfile" | cut -d' ' -f1)
    [[ -f "$lockfile_hash_file" ]] && cached_hash=$(cat "$lockfile_hash_file")

    if [[ ! -d "$root_dir/node_modules" || "$current_hash" != "$cached_hash" ]]; then
        info "安装前端依赖（根 workspace）..."
        (cd "$root_dir" && pnpm install --frozen-lockfile) || {
            error "前端依赖安装失败"
            return 1
        }
        echo "$current_hash" > "$lockfile_hash_file"
        rm -rf "$web_dir/.next/cache"
        success "前端依赖安装完成"
    fi

    # 启动前端（后台运行，日志输出到文件）
    # 走 Bazel 的 next_dev devserver — 这样 @agentsmesh/* 的 internal
    # packages 通过 //:node_modules/@agentsmesh/<name> 链接进 sandbox，
    # Next.js 能正常解析。源码 pnpm node_modules 没有这些链接（pure
    # Bazel 接管 internal packages 之后），不能直接 `next dev`。
    local log_file="$SCRIPT_DIR/web.log"
    info "启动前端服务 (端口: $web_port, Bazel devserver)..."
    local saved_dir="$PWD"
    cd "$root_dir"
    bazel run //clients/web:next_dev -- --port "$web_port" > "$log_file" 2>&1 < /dev/null &
    disown $!
    cd "$saved_dir"

    # 等待前端启动
    local max_wait=60
    for ((i=1; i<=max_wait; i++)); do
        if curl -s "http://localhost:$web_port" &>/dev/null; then
            success "前端服务已启动 (http://localhost:$web_port)"
            return 0
        fi
        sleep 1
    done

    warn "前端服务启动中，请稍后访问 http://localhost:$web_port"
    echo "  查看日志: tail -f $log_file"
}

