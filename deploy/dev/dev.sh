# 等待服务就绪
wait_for_service() {
    local container="$1"
    local check_cmd="$2"
    local max_retries=30

    for ((i=1; i<=max_retries; i++)); do
        if docker exec "$container" $check_cmd &>/dev/null; then
            return 0
        fi
        sleep 2
    done
    return 1
}

# 执行数据库迁移
run_migrations() {
    local backend_container="${COMPOSE_PROJECT_NAME}-backend-1"
    local db_url="postgres://agentsmesh:agentsmesh_dev@postgres:5432/agentsmesh?sslmode=disable"

    info "检查数据库迁移状态..."

    # 等待 backend 容器就绪（migrate 工具在 backend 容器中）
    local max_wait=30
    for ((i=1; i<=max_wait; i++)); do
        if docker exec "$backend_container" migrate -version &>/dev/null; then
            break
        fi
        sleep 1
    done

    # 检查迁移状态
    local migration_output
    migration_output=$(docker exec "$backend_container" migrate \
        -path /app/migrations \
        -database "$db_url" \
        version 2>&1) || true

    # 检查是否有 dirty 状态
    if echo "$migration_output" | grep -q "dirty"; then
        warn "检测到迁移状态为 dirty，尝试修复..."
        local dirty_version
        dirty_version=$(echo "$migration_output" | grep -oE '[0-9]+' | head -1)
        if [[ -n "$dirty_version" ]]; then
            docker exec "$backend_container" migrate \
                -path /app/migrations \
                -database "$db_url" \
                force "$dirty_version" 2>/dev/null || true
            success "已修复 dirty 状态 (version: $dirty_version)"
        fi
    fi

    # 执行迁移
    info "执行数据库迁移..."
    local migrate_result
    migrate_result=$(docker exec "$backend_container" migrate \
        -path /app/migrations \
        -database "$db_url" \
        up 2>&1) || true

    # 检查迁移结果
    # 注意: 使用 "^error" 匹配行首，避免误匹配迁移文件名中包含 "error" 的条目
    # (如 000046_add_pod_error_fields)
    if echo "$migrate_result" | grep -q "no change"; then
        info "数据库已是最新版本"
    elif echo "$migrate_result" | grep -q "^error"; then
        # 如果迁移失败，可能是部分迁移已手动完成，尝试逐个跳过
        warn "迁移遇到错误，尝试继续..."
        local current_version
        current_version=$(docker exec "$backend_container" migrate \
            -path /app/migrations \
            -database "$db_url" \
            version 2>&1 | grep -oE '^[0-9]+' || echo "0")

        # 获取最新迁移版本
        local latest_version
        latest_version=$(ls -1 "$MIGRATIONS_DIR"/*.up.sql 2>/dev/null | \
            sed 's/.*\/\([0-9]*\)_.*/\1/' | sort -n | tail -1)

        if [[ -n "$latest_version" && "$current_version" != "$latest_version" ]]; then
            # 尝试强制设置到最新版本（仅在开发环境使用）
            warn "跳过失败的迁移，强制设置版本为 $latest_version"
            docker exec "$backend_container" migrate \
                -path /app/migrations \
                -database "$db_url" \
                force "$latest_version" 2>/dev/null || true
        fi
    else
        success "数据库迁移完成"
    fi

    # 显示最终版本
    local final_version
    final_version=$(docker exec "$backend_container" migrate \
        -path /app/migrations \
        -database "$db_url" \
        version 2>&1 | head -1)
    info "当前迁移版本: $final_version"
}

# 初始化 seed 数据
init_seed() {
    local pg_container="$1"

    # 检查是否已有 seed 数据
    local user_exists
    user_exists=$(docker exec "$pg_container" psql -U agentsmesh -d agentsmesh -t -c \
        "SELECT COUNT(*) FROM users WHERE email = 'dev@agentsmesh.local'" 2>/dev/null | tr -d ' ')

    if [[ "$user_exists" -gt 0 ]]; then
        info "Seed 数据已存在，跳过"
        return 0
    fi

    info "初始化 seed 数据..."
    docker exec -i "$pg_container" psql -U agentsmesh -d agentsmesh < "$SEED_FILE" &>/dev/null

    # 应用 LemonSqueezy Variant IDs 配置
    if [[ -f "$LEMONSQUEEZY_SEED_FILE" ]]; then
        info "配置 LemonSqueezy Variant IDs..."
        docker exec -i "$pg_container" psql -U agentsmesh -d agentsmesh < "$LEMONSQUEEZY_SEED_FILE" &>/dev/null
    fi

    success "Seed 数据初始化完成"
}

# 初始化 Gitea Git 服务器
init_gitea() {
    local gitea_container="${COMPOSE_PROJECT_NAME}-gitea-1"
    source "$ENV_FILE"
    local gitea_port="${GITEA_HTTP_PORT:-3001}"

    # Wait for Gitea healthcheck
    info "等待 Gitea 就绪..."
    local max_retries=30
    for ((i=1; i<=max_retries; i++)); do
        if curl -s "http://localhost:${gitea_port}/api/v1/version" &>/dev/null; then
            break
        fi
        if [[ $i -eq $max_retries ]]; then
            warn "Gitea 启动超时，跳过初始化"
            return 0
        fi
        sleep 2
    done
    success "Gitea 已就绪"

    # Run init script
    "$SCRIPT_DIR/gitea/init-gitea.sh" "$gitea_container" "$gitea_port"
}

# Generate the runner SSH keypair used to access Gitea repos.
# The private key is never committed; if it's missing (first clone, new machine),
# generate a fresh pair. init_gitea will register the new public key with Gitea.
#
# Also validates existing key pairs: if id_ed25519 and id_ed25519.pub are
# mismatched (e.g., one was regenerated without the other), SSH will refuse to
# authenticate with "identity_sign: private key contents do not match public".
# We detect this and regenerate both files to fix it automatically.
generate_runner_ssh_key() {
    local ssh_dir="$SCRIPT_DIR/runner-ssh"
    local private_key="$ssh_dir/id_ed25519"
    local public_key="$ssh_dir/id_ed25519.pub"

    if [[ -f "$private_key" ]]; then
        # Validate that the key pair is consistent.
        # OpenSSH will reject the key with a cryptic "contents do not match
        # public" error if the .pub file is stale or missing.
        local needs_regen=false

        if [[ ! -f "$public_key" ]]; then
            warn "Public key missing, will regenerate SSH key pair"
            needs_regen=true
        else
            # Derive the public key from the private key and compare with .pub file.
            # Only compare key type + key data (fields 1-2), ignoring the comment.
            local derived_key stored_key
            derived_key=$(ssh-keygen -y -f "$private_key" 2>/dev/null | awk '{print $1, $2}')
            stored_key=$(awk '{print $1, $2}' "$public_key" 2>/dev/null)

            if [[ -z "$derived_key" || "$derived_key" != "$stored_key" ]]; then
                warn "SSH key pair mismatch detected, will regenerate"
                needs_regen=true
            fi
        fi

        if [[ "$needs_regen" == true ]]; then
            rm -f "$private_key" "$public_key"
        else
            chmod 600 "$private_key"
            info "Runner SSH key already exists and is valid"
            return 0
        fi
    fi

    info "Generating runner SSH key (private key not committed)..."
    ssh-keygen -t ed25519 -C "agentsmesh-dev-runner@local" -f "$private_key" -N "" > /dev/null
    chmod 600 "$private_key"
    success "Runner SSH key generated: $private_key"
}

# Configure ~/.ssh/config so that local runners (running on the host, outside Docker)
# can clone git@gitea:org/repo.git URLs. Inside Docker, "gitea" resolves via Docker DNS.
# On the host, SSH would fail with "nodename not known" — we fix this by adding a managed
# Host block that maps "gitea" → 127.0.0.1 on the worktree-specific SSH port.
#
# Safe to re-run: removes the old managed block and rewrites it with the current port.
setup_gitea_ssh_config() {
    source "$ENV_FILE"
    local ssh_dir="$HOME/.ssh"
    local ssh_config="$ssh_dir/config"
    local gitea_ssh_port="${GITEA_SSH_PORT:-2222}"
    local identity_file="$SCRIPT_DIR/runner-ssh/id_ed25519"
    local marker_start="# BEGIN AgentsMesh dev gitea"
    local marker_end="# END AgentsMesh dev gitea"

    mkdir -p "$ssh_dir"
    chmod 700 "$ssh_dir"
    [[ -f "$ssh_config" ]] || touch "$ssh_config"
    chmod 600 "$ssh_config"

    # Remove any existing managed block so we can re-append with the current port.
    local tmp
    tmp=$(mktemp)
    awk "/^${marker_start}$/,/^${marker_end}$/{next} {print}" "$ssh_config" > "$tmp"
    cat "$tmp" > "$ssh_config"
    rm -f "$tmp"

    cat >> "$ssh_config" << EOF

${marker_start}
Host gitea
    HostName 127.0.0.1
    Port ${gitea_ssh_port}
    IdentityFile ${identity_file}
    IdentitiesOnly yes
    StrictHostKeyChecking no
    UserKnownHostsFile /dev/null
${marker_end}
EOF

    success "SSH config: git@gitea:... → 127.0.0.1:${gitea_ssh_port} (key: runner-ssh/id_ed25519)"
}

# Kill all stale runner-related processes and restart Docker runner/relay containers.
# Use this when runners are in a bad state and you want a clean slate.
reset_runners() {
    if [[ -f "$ENV_FILE" ]]; then
        source "$ENV_FILE"
    fi

    echo ""
    echo "=========================================="
    echo "  Reset Runner Services"
    echo "=========================================="
    echo ""

    # 1. Kill local agentsmesh-runner process
    if pgrep -f "agentsmesh-runner" &>/dev/null; then
        info "停止本地 agentsmesh-runner 进程..."
        pkill -f "agentsmesh-runner" 2>/dev/null || true
        sleep 1
        pkill -9 -f "agentsmesh-runner" 2>/dev/null || true
        success "本地 runner 进程已停止"
    else
        info "未发现本地 agentsmesh-runner 进程"
    fi

    # 2. Kill anything holding runner-related ports (orphaned after ungraceful kills)
    local runner_ports=(19000 19080 9090)  # MCP server, web console, health check
    for port in "${runner_ports[@]}"; do
        local pids
        pids=$(lsof -ti :"$port" 2>/dev/null) || true
        if [[ -n "$pids" ]]; then
            info "释放端口 $port (PID: $pids)..."
            echo "$pids" | xargs kill -9 2>/dev/null || true
            success "端口 $port 已释放"
        fi
    done

    # 3. Start Docker Desktop if not running
    if ! docker info &>/dev/null 2>&1; then
        info "Docker 未运行，正在启动 Docker Desktop..."
        open -a Docker
        local max_wait=30
        for ((i=1; i<=max_wait; i++)); do
            if docker info &>/dev/null 2>&1; then
                success "Docker 已启动"
                break
            fi
            if [[ $i -eq $max_wait ]]; then
                warn "Docker 启动超时，跳过容器重启"
                echo ""
                success "本地 runner 进程已清理，请手动启动 Docker 后运行："
                echo "  agentsmesh-runner run"
                return 0
            fi
            sleep 2
        done
    fi

    # 4. Restart Docker runner and relay containers
    cd "$SCRIPT_DIR"
    info "重启 Docker runner 和 relay 容器..."
    docker compose up -d --force-recreate runner relay 2>&1 | grep -v "^#" | grep -v warning || true

    # Wait for relay to become healthy
    local max_wait=20
    for ((i=1; i<=max_wait; i++)); do
        local relay_status
        relay_status=$(docker compose ps relay 2>/dev/null | grep relay | awk '{print $NF}')
        if echo "$relay_status" | grep -q "healthy"; then
            success "Docker runner 和 relay 已就绪"
            break
        fi
        if [[ $i -eq $max_wait ]]; then
            warn "Relay 健康检查超时，可能仍在启动中"
        fi
        sleep 2
    done

    # 5. Auto-fix stale grpc_endpoint in local runner config
    local runner_config="$HOME/.agentsmesh/config.yaml"
    if [[ -f "$runner_config" ]] && command -v agentsmesh-runner &>/dev/null; then
        local current_endpoint new_endpoint grpc_port
        current_endpoint=$(grep "grpc_endpoint:" "$runner_config" | awk '{print $2}')
        grpc_port="${GRPC_PORT:-10001}"
        new_endpoint="grpcs://127.0.0.1:$grpc_port"
        if [[ "$current_endpoint" != "$new_endpoint" ]]; then
            info "修复本地 runner 配置中的 grpc_endpoint..."
            sed -i '' "s|grpc_endpoint:.*|grpc_endpoint: $new_endpoint|" "$runner_config"
            success "grpc_endpoint 已更新: $current_endpoint → $new_endpoint"
        fi
    fi

    echo ""
    success "Runner 服务已重置！如需使用本地 runner，请运行："
    echo "  agentsmesh-runner run"
    echo ""
}

# 清理环境
clean() {
    # 读取端口配置
    if [[ -f "$ENV_FILE" ]]; then
        source "$ENV_FILE"
    fi
    local web_port="${WEB_PORT:-3000}"

    local web_admin_port="${WEB_ADMIN_PORT:-3001}"

    # 停止前端进程
    if lsof -i :"$web_port" &>/dev/null; then
        info "停止前端服务 (端口: $web_port)..."
        lsof -ti :"$web_port" | xargs kill -9 2>/dev/null || true
        success "前端服务已停止"
    fi

    # 停止 Admin Console 进程
    if lsof -i :"$web_admin_port" &>/dev/null; then
        info "停止 Admin Console (端口: $web_admin_port)..."
        lsof -ti :"$web_admin_port" | xargs kill -9 2>/dev/null || true
        success "Admin Console 已停止"
    fi

    # 清理前端日志
    rm -f "$SCRIPT_DIR/web.log"
    rm -f "$SCRIPT_DIR/web-admin.log"

    if [[ -f "$ENV_FILE" ]]; then
        info "清理 Docker 环境: ${COMPOSE_PROJECT_NAME:-agentsmesh}..."
        cd "$SCRIPT_DIR"
        docker compose down -v --remove-orphans 2>/dev/null || true
        rm -f "$ENV_FILE"
        success "清理完成"
    else
        warn "Docker 环境未初始化"
    fi
}

