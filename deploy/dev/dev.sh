# All host-side runtime state lives under deploy/dev/runtime/ (gitignored).
# Each service has its own pid file + log file. Runner gets an isolated
# HOME so its `~/.claude.json` etc. don't pollute the developer's real
# config. Workspaces / certs also live here per-worktree.
_runtime_dir() { echo "$SCRIPT_DIR/runtime"; }

check_ibazel_doctor() {
    local missing=()
    command -v bazel >/dev/null 2>&1 || missing+=("bazel (bazelisk)")
    if (( ${#missing[@]} > 0 )); then
        error "缺少必需工具：${missing[*]}"
        echo "  macOS: brew install bazelisk"
        echo "  Linux: 见 https://github.com/bazelbuild/bazelisk/releases"
        exit 1
    fi

    # ibazel is *recommended* (provides hot reload) but not mandatory.
    # CI runs dev.sh without ibazel and falls back to one-shot `bazel run`
    # in `_launch_ibazel` (see HAS_IBAZEL detection there).
    if ! command -v ibazel >/dev/null 2>&1; then
        warn "ibazel 未安装，host service 将退化为单次 bazel run（无 hot reload）"
        echo "  macOS: brew install bazel-watcher"
        echo "  Linux: 见 https://github.com/bazelbuild/bazel-watcher/releases"
    fi

    # AI CLI tools are required by the runner. Warn (don't fail) so
    # backend / relay can still come up for non-pod work.
    local ai_missing=()
    command -v claude >/dev/null 2>&1 || ai_missing+=("claude")
    command -v codex >/dev/null 2>&1 || ai_missing+=("codex")
    command -v gemini >/dev/null 2>&1 || ai_missing+=("gemini")
    if (( ${#ai_missing[@]} > 0 )); then
        warn "AI CLI 未全装（${ai_missing[*]}）— runner pod 创建会受限"
        echo "  npm i -g @anthropic-ai/claude-code @openai/codex @google/gemini-cli"
    fi
}

# Generate runner mTLS client cert (replaces runner-entrypoint.sh logic).
# Cert lives under runtime/runner/certs/ — re-generated only if missing.
# NOTE: kept as a host-side helper for tools that need to inspect the
# cert from outside the runner container, but the runner container has
# its own copy (mounted via runner-entrypoint.sh). Not called from
# main() anymore.
generate_runner_cert() {
    local rt_dir="$(_runtime_dir)/runner"
    local certs_dir="$rt_dir/certs"
    local ssl_dir="$SCRIPT_DIR/ssl"
    local node_id="${RUNNER_NODE_ID:-dev-runner}"
    local org_slug="${RUNNER_ORG_SLUG:-dev-org}"

    mkdir -p "$certs_dir"

    if [[ -f "$certs_dir/runner.crt" && -f "$certs_dir/runner.key" ]]; then
        return 0
    fi
    if [[ ! -f "$ssl_dir/ca.crt" || ! -f "$ssl_dir/ca.key" ]]; then
        error "CA 证书未找到: $ssl_dir (先跑 generate_ssl_certs)"
        return 1
    fi

    info "生成 Runner mTLS 客户端证书..."
    openssl genpkey -algorithm EC -pkeyopt ec_paramgen_curve:prime256v1 \
        -out "$certs_dir/runner.key" 2>/dev/null
    openssl req -new -key "$certs_dir/runner.key" \
        -out "$certs_dir/runner.csr" \
        -subj "/CN=${node_id}/O=${org_slug}/OU=Runner" 2>/dev/null
    cat > "$certs_dir/runner_ext.cnf" << 'EOF'
authorityKeyIdentifier=keyid,issuer
basicConstraints=CA:FALSE
keyUsage = digitalSignature, keyEncipherment
extendedKeyUsage = clientAuth
EOF
    openssl x509 -req -days 365 \
        -in "$certs_dir/runner.csr" \
        -CA "$ssl_dir/ca.crt" -CAkey "$ssl_dir/ca.key" \
        -CAserial "$certs_dir/ca.srl" -CAcreateserial \
        -out "$certs_dir/runner.crt" \
        -extfile "$certs_dir/runner_ext.cnf" 2>/dev/null
    cp "$ssl_dir/ca.crt" "$certs_dir/ca.crt"
    rm -f "$certs_dir/runner.csr" "$certs_dir/runner_ext.cnf"
    chmod 600 "$certs_dir/runner.key"
    chmod 644 "$certs_dir/runner.crt" "$certs_dir/ca.crt"
    success "Runner 证书生成完成 ($certs_dir)"
}

# Build the runner binary via Bazel (linux/amd64 cross-compile).
# Output: bazel-bin/runner/cmd/runner/runner_/runner. We then copy it
# to deploy/dev/runner-binary so the docker build context (which is
# `deploy/dev`) can reach it — bazel-bin is a symlink to /private/var/...
# that docker build cannot follow across.
build_runner_binary() {
    info "Bazel build runner binary (linux/amd64)..."
    local repo_root="$SCRIPT_DIR/../.."
    (
        cd "$repo_root"
        bazel build //runner/cmd/runner:runner \
            --platforms=@rules_go//go/toolchain:linux_amd64
    ) || {
        error "bazel build runner 失败"
        return 1
    }
    # `cp -L` dereferences the bazel-bin symlink chain to copy the
    # actual ELF binary into the docker build context. Remove first
    # because bazel outputs are read-only and a previous copy will
    # block overwrite.
    rm -f "$SCRIPT_DIR/runner-binary"
    cp -L "$repo_root/bazel-bin/runner/cmd/runner/runner_/runner" \
        "$SCRIPT_DIR/runner-binary"
    chmod +x "$SCRIPT_DIR/runner-binary"
    success "Runner binary 已编译并复制到 build context"
}


# Wait for an HTTP endpoint to return success.
_wait_http() {
    local url="$1" name="$2" max="${3:-40}"
    for ((i=1; i<=max; i++)); do
        if curl -sf "$url" >/dev/null 2>&1; then return 0; fi
        sleep 1
    done
    error "$name 健康检查超时 ($url)"
    return 1
}

# Background-launch a service. Uses ibazel (hot reload) when installed;
# falls back to plain `bazel run` for CI / minimal-tooling environments.
# Args: name target [extra args...]. Writes pid + log under runtime/<name>/.
_launch_ibazel() {
    local name="$1" target="$2"; shift 2
    local rt_dir="$(_runtime_dir)/$name"
    mkdir -p "$rt_dir"
    local pid_file="$rt_dir/$name.pid"
    local log_file="$rt_dir/$name.log"

    # Reap any orphan from a previous run.
    if [[ -f "$pid_file" ]]; then
        local old=$(cat "$pid_file")
        if kill -0 "$old" 2>/dev/null; then
            kill -TERM "$old" 2>/dev/null || true
            sleep 1
        fi
        rm -f "$pid_file"
    fi

    local launcher
    if command -v ibazel >/dev/null 2>&1; then
        launcher="ibazel"
    else
        launcher="bazel"
    fi
    info "启动 host service: $name (target: $target, launcher: $launcher)"
    local repo_root="$SCRIPT_DIR/../.."
    (
        cd "$repo_root"
        nohup $launcher run "$target" "$@" > "$log_file" 2>&1 &
        echo $! > "$pid_file"
        disown
    )
}

