# 主流程
main() {
    cd "$SCRIPT_DIR"

    # 处理 --clean 参数
    if [[ "${1:-}" == "--clean" || "${1:-}" == "-c" ]]; then
        clean
        exit 0
    fi

    # 处理 --reset-runners 参数
    if [[ "${1:-}" == "--reset-runners" || "${1:-}" == "--kill-runners" || "${1:-}" == "--rebuild-runner" ]]; then
        reset_runners
        exit 0
    fi

    # `--backend-only`: skip frontend startup (Web + Web-Admin). For CI
    # e2e jobs that bring up the docker stack here and start the Web
    # dev-server separately under Bazel.
    local backend_only=false
    if [[ "${1:-}" == "--backend-only" ]]; then
        backend_only=true
    fi

    # 显示帮助
    if [[ "${1:-}" == "--help" || "${1:-}" == "-h" ]]; then
        echo "用法: $0 [选项]"
        echo ""
        echo "选项:"
        echo "  无参数             一键启动完整开发环境"
        echo "                       - docker: postgres/redis/minio/traefik/runner 等基础设施"
        echo "                       - host:   backend / relay / web / web-admin (ibazel + next dev)"
        echo "  --backend-only     仅启动 docker 基础设施 + host backend/relay (CI 用)"
        echo "  --rebuild-runner   重 build runner binary (bazel) + 重启 runner 容器"
        echo "  --clean            停止并清理所有服务"
        echo ""
        echo "  改动 backend / relay 源码: ibazel 自动重 build (host)"
        echo "  改动 runner 源码:        ./dev.sh --rebuild-runner"
        echo ""
        echo "前端日志: tail -f deploy/dev/web.log"
        exit 0
    fi

    echo ""
    echo "=========================================="
    echo "  AgentsMesh 开发环境初始化"
    echo "=========================================="
    echo ""

    # Step 1: 生成 SSL 证书
    generate_ssl_certs

    # Step 2: 生成 AI CLI 配置（保留：CI / 工具脚本仍可能引用 deploy/dev/ai-cli-configs/）
    generate_ai_cli_configs

    # Step 3: 生成配置
    generate_env
    source "$ENV_FILE"

    # Step 3.5: ibazel doctor — 早失败比起容器起来后报错更友好
    check_ibazel_doctor

    # Step 4: 生成 Traefik 配置（动态网络名 + host 端口）
    generate_traefik_config

    # Step 5: 生成 clients/web/.env.local（支持本地前端开发）
    generate_web_env

    # Step 5.5: 生成 clients/web-admin/.env.local（支持本地 Admin Console 开发）
    generate_web_admin_env

    # Step 5.8: Generate runner SSH key pair (host runner 也需要它做 git 操作)
    generate_runner_ssh_key

    # Step 5.9: Build runner binary via Bazel (linux/amd64). Required
    # before docker compose up — runner.Dockerfile COPYs the binary
    # from bazel-bin/ into the dev image.
    build_runner_binary

    # Step 6: 启动 Docker 基础设施 + runner (postgres / redis / minio / traefik / jaeger / gitea / otelcol / adminer / runner)
    info "启动 Docker 基础设施 + runner (首次可能需要几分钟)..."
    local up_attempt=0
    local up_max=3
    while [ $up_attempt -lt $up_max ]; do
        up_attempt=$((up_attempt + 1))
        if docker compose up -d --build --quiet-pull 2>&1 | grep -v "^#" | grep -v "^\[" | grep -v "^$"; then
            break
        fi
        if [ $up_attempt -eq $up_max ]; then
            error "Docker compose up failed after $up_max attempts"
            exit 1
        fi
        warn "compose up failed (attempt $up_attempt/$up_max) — retrying in 10s"
        sleep 10
    done
    success "Docker 基础设施已启动"

    # Step 7: 等待 PostgreSQL
    local pg_container="${COMPOSE_PROJECT_NAME}-postgres-1"
    info "等待 PostgreSQL 就绪..."
    if ! wait_for_service "$pg_container" "pg_isready -U agentsmesh"; then
        error "PostgreSQL 启动超时"
        exit 1
    fi
    success "PostgreSQL 已就绪"

    # Step 8: 执行迁移 (docker compose run --rm migrate — backend 不在 docker 跑了)
    run_migrations

    # Step 9: 初始化 seed
    init_seed "$pg_container"

    # Step 9.5: 初始化 Gitea Git 服务器 (SSH key already generated at Step 5.8)
    init_gitea

    # Step 9.6: Configure local SSH so git@gitea:... works outside Docker
    setup_gitea_ssh_config

    # Step 10: 启动 host-side backend + relay via ibazel.
    # backend 必须先起 — runner 容器通过 traefik:9443 (mTLS gRPC) 连
    # backend，traefik passthrough 到 host backend。
    start_backend_host
    start_relay_host

    # Step 11: 启动本地前端
    if [[ "$backend_only" == "true" ]]; then
        info "--backend-only: skipping frontend startup"
    else
        start_frontend

        # Step 11.5: 启动本地 Admin Console
        start_admin_frontend
    fi

    # 显示结果
    show_result
}

main "$@"
