# 生成 Traefik 配置（动态设置网络名 + host-side service 端口）
generate_traefik_config() {
    local worktree_name="${WORKTREE_NAME:-main}"
    local project_name="agentsmesh-${worktree_name}"
    local traefik_yml="$SCRIPT_DIR/traefik/traefik.yml"
    local dynamic_dir="$SCRIPT_DIR/traefik/dynamic"
    mkdir -p "$dynamic_dir"

    cat > "$traefik_yml" << EOF
# Traefik Static Configuration for Development
# Auto-generated by dev.sh - project: $project_name
#
# After the air → ibazel switch, backend / runner / relay run on the
# developer host (not in docker). Traefik discovers them via the file
# provider only — docker provider was removed because no relevant
# services are in the compose network anymore.

api:
  dashboard: true
  insecure: true

entryPoints:
  web:
    address: ":80"
  grpc:
    address: ":9443"

providers:
  file:
    directory: /etc/traefik/dynamic
    watch: true

log:
  level: DEBUG
  format: common

accessLog:
  format: common
EOF

    # Dynamic HTTP routing — points at host backend / relay through
    # host.docker.internal. Ports come from .env (worktree-scoped).
    cat > "$dynamic_dir/http.yml" << EOF
# HTTP routing — Auto-generated by dev.sh
# host services listen on worktree-scoped ports; traefik proxies in
# from :HTTP_PORT and forwards via host.docker.internal.

http:
  routers:
    backend-api:
      entryPoints:
        - web
      rule: "PathPrefix(\`/api\`) || PathPrefix(\`/health\`)"
      service: backend-api
      priority: 100

    relay:
      entryPoints:
        - web
      rule: "PathPrefix(\`/relay\`)"
      service: relay
      middlewares:
        - relay-strip
      priority: 30

  middlewares:
    relay-strip:
      stripPrefix:
        prefixes:
          - "/relay"

  services:
    backend-api:
      loadBalancer:
        servers:
          - url: "http://host.docker.internal:${BACKEND_HTTP_PORT}"

    relay:
      loadBalancer:
        servers:
          - url: "http://host.docker.internal:${RELAY_HTTP_PORT}"
EOF

    # Dynamic gRPC mTLS passthrough → host backend
    cat > "$dynamic_dir/grpc.yml" << EOF
# gRPC mTLS passthrough — Auto-generated by dev.sh
# Backend handles mTLS verification; traefik just tunnels TCP+TLS.

tcp:
  routers:
    grpc-mtls:
      entryPoints:
        - grpc
      rule: "HostSNI(\`*\`)"
      service: backend-grpc
      tls:
        passthrough: true

  services:
    backend-grpc:
      loadBalancer:
        servers:
          - address: "host.docker.internal:${BACKEND_GRPC_PORT}"
EOF
    success "生成 Traefik 配置 (host backend=:${BACKEND_HTTP_PORT} grpc=:${BACKEND_GRPC_PORT} relay=:${RELAY_HTTP_PORT})"
}

