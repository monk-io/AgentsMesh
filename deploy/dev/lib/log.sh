# shellcheck shell=bash
# log.sh — colored leveled logger.
# info / success / warn / error all emit to stdout (warn/error to stderr would
# break dev.sh's control flow, which uses set -e and reads from stdout).

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

info()    { echo -e "${BLUE}[INFO]${NC} $1"; }
success() { echo -e "${GREEN}[OK]${NC} $1"; }
warn()    { echo -e "${YELLOW}[WARN]${NC} $1"; }
error()   { echo -e "${RED}[ERROR]${NC} $1"; }
